package Lab13Package;

public class SumOfIntegers
{
	public static void main(String[]args)
	{
		int number = 10;
		int total = 0;
		for( number = 10; number <= 20;  number++)
		{
			
			total += number; 
		}
		
		System.out.println(total);
	}
	
}

